package com.theyaku;

public class Kitchen {

    private int stove;
    private int plate;
    private int knife;

    public Kitchen(int stove, int plate, int knife) {
        this.stove = stove;
        this.plate = plate;
        this.knife = knife;
    }

    public int getStove() {
        return stove;
    }

    public int getPlate() {
        return plate;
    }

    public int getKnife() {
        return knife;
    }


}
